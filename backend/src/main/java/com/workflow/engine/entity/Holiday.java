package com.workflow.engine.entity;

import jakarta.persistence.*;
import java.time.LocalDate;

@Entity
@Table(name = "holidays")
public class Holiday {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false)
    @jakarta.validation.constraints.NotBlank(message = "Holiday name is required")
    private String name;

    @Column(nullable = false)
    @jakarta.validation.constraints.NotNull(message = "Holiday date is required")
    private LocalDate date;

    /** e.g. "Public", "Company", "Optional" */
    @jakarta.validation.constraints.NotBlank(message = "Holiday type is required")
    private String type;

    public Holiday() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public LocalDate getDate() {
        return date;
    }

    public void setDate(LocalDate date) {
        this.date = date;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
